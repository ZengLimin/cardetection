function doQuery(noCapt){
	var modbileVal = $("#q_mobile").val();
	if (modbileVal == null || modbileVal == "") {
		$(".errorTip").html("手机号码不能为空");
		return false;
	} else if (!/^0?(13|15|18)[0-9]{9}$/.test(modbileVal)) {
		$(".errorTip").html("手机号码格式不正确");
		return false;
	} 
	var password = $("#password").val();
	if (password == null || password == "") {
		$(".errorTip").html("查询密码不能为空");
		return false;
	}

	if(noCapt || checkCapt('errorTip','leftCaptcha')){
	var validate = $('.leftCaptcha').val();
	$.ajax({
		type : "POST",
		async : false,
		url : "query.php?d=" + new Date()*1,
		data : {
			"mobile" : modbileVal,
			"password" : password,
			"validate":validate,
			"nocapt":noCapt
		},
		success : function(msg) {
			//alert(msg);
			/* $("#dialog").html(msg);
			$( "#dialog" ).dialog({
			      modal: true
			    }); */
			if(msg){
				if(msg ==1 ){
					$(".errorTip").html('验证码输入错误或者已过期');
				}else{
				$(".bookinfo").html(msg);
				//$(".yibuSmartViewMargin").html("");
				$("#book_query")[0].reset();
				}
			}else{
				$(".errorTip").html('该号码未预约或者查询密码错误');
			}    
			$("#leftCaptcha_img").attr("src", 'vcode.php');
			$("#leftCaptchaflag").val(1);
		},
		error: function() {  
			$(".errorTip").html("系统出错！");
	    }
	});
   }
	
}
</script>